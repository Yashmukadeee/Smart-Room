-- Create appliances/devices table
CREATE TABLE IF NOT EXISTS public.devices (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL, -- 'light', 'ac', 'fan', 'lock'
    status BOOLEAN DEFAULT false NOT NULL,
    value INTEGER DEFAULT 0, -- e.g., brightness or temperature
    color TEXT DEFAULT '#ffffff', -- RGB color hex value
    room TEXT DEFAULT 'Bedroom',
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

