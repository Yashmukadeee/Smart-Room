-- -----------------------------------------------
-- Create todos table for Alexa / dashboard tasks
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS public.todos (
    id TEXT PRIMARY KEY,                   -- UUID generated client-side
    title TEXT NOT NULL,
    completed BOOLEAN DEFAULT false NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable Realtime Subscriptions for todos table
-- ALTER PUBLICATION supabase_realtime ADD TABLE public.todos;

-- Enable Row Level Security (RLS) on todos
ALTER TABLE public.todos ENABLE ROW LEVEL SECURITY;

-- Allow anon clients to read todos (dashboard display)
CREATE POLICY "Allow public read todos" ON public.todos
    FOR SELECT TO anon USING (true);

-- Allow anon clients to insert todos (Alexa bridge inserts via dashboard webhook)
CREATE POLICY "Allow public insert todos" ON public.todos
    FOR INSERT TO anon WITH CHECK (true);

-- Allow anon clients to update todos (toggle completion from dashboard)
CREATE POLICY "Allow public update todos" ON public.todos
    FOR UPDATE TO anon USING (true) WITH CHECK (true);

-- Allow anon clients to delete todos (remove from dashboard)
CREATE POLICY "Allow public delete todos" ON public.todos
    FOR DELETE TO anon USING (true);
