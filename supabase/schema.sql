-- Create commands table for Alexa Speech commands
CREATE TABLE IF NOT EXISTS public.commands (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable Realtime Subscriptions for commands table
-- Note: If publication doesn't exist, this statement might need project editor execution
-- ALTER PUBLICATION supabase_realtime ADD TABLE public.commands;

-- Enable Row Level Security (RLS) on commands
ALTER TABLE public.commands ENABLE ROW LEVEL SECURITY;

-- Allow read-only (SELECT) access for public users/anonymous clients (for real-time dashboard listeners)
CREATE POLICY "Allow public read commands" ON public.commands
    FOR SELECT TO anon USING (true);

-- Allow insert (INSERT) access for public/anonymous clients (so Alexa/IFTTT webhooks can publish directly if needed)
CREATE POLICY "Allow public insert commands" ON public.commands
    FOR INSERT TO anon WITH CHECK (true);


-- Create sensor_log table for ESP32 climate readings
CREATE TABLE IF NOT EXISTS public.sensor_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    device_id TEXT NOT NULL DEFAULT 'esp32_dht11',
    temp_c NUMERIC(5,2),
    humidity NUMERIC(5,2),
    recorded_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable Realtime Subscriptions for sensor_log table
