ON public.sensor_log
FOR SELECT 
TO authenticated 
USING (
    EXISTS (
        SELECT 1 FROM public.devices d
        JOIN public.room_users ru ON d.room = ru.room
        WHERE d.id = sensor_log.device_id 
        AND ru.user_id = auth.uid()
    )
);

CREATE POLICY "Allow ESP32 to insert climate logs"
ON public.sensor_log
FOR INSERT
WITH CHECK (
    public.check_esp32_auth()
    OR auth.role() = 'authenticated'
);

-- ----------------------------------------------------
-- 6. Server-Side Automations Engine (Trigger & Function)
-- ----------------------------------------------------
CREATE OR REPLACE FUNCTION public.handle_device_automation()
RETURNS TRIGGER AS $$
DECLARE
    rule RECORD;
BEGIN
    -- Only run when status changes to avoid infinite recursion
    IF (OLD.status IS DISTINCT FROM NEW.status) THEN
        -- Loop through active rules triggered by this device
        FOR rule IN 
            SELECT action_device_id, action_state 
            FROM public.automation_rules 
            WHERE trigger_device_id = NEW.id 
              AND trigger_state = NEW.status 
              AND is_active = true
        LOOP
            -- Update the target device status
            UPDATE public.devices 
            SET status = rule.action_state,
                updated_at = timezone('utc'::text, now())
            WHERE id = rule.action_device_id;
        END LOOP;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Attach Trigger to devices table
DROP TRIGGER IF EXISTS trg_device_automation ON public.devices;
CREATE TRIGGER trg_device_automation
AFTER UPDATE ON public.devices
FOR EACH ROW
EXECUTE FUNCTION public.handle_device_automation();

