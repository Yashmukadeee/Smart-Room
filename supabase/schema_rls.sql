-- ----------------------------------------------------
-- 1. Setup Support Tables (room_users, hardware_tokens, automation_rules)
-- ----------------------------------------------------

CREATE TABLE IF NOT EXISTS public.room_users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    room TEXT NOT NULL DEFAULT 'Master Bedroom',
    pin_hash TEXT NOT NULL, -- SHA-256 hash of PIN
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    UNIQUE(user_id, room)
);

CREATE TABLE IF NOT EXISTS public.hardware_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    token TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Insert default ESP32 hardware token
INSERT INTO public.hardware_tokens (name, token)
VALUES ('esp32', 'esp32_secure_token_123456')
ON CONFLICT (name) DO NOTHING;

CREATE TABLE IF NOT EXISTS public.automation_rules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    room TEXT NOT NULL DEFAULT 'Master Bedroom',
    trigger_device_id TEXT NOT NULL REFERENCES public.devices(id) ON DELETE CASCADE,
    trigger_state BOOLEAN NOT NULL, -- true = Open/ON, false = Closed/OFF
    action_device_id TEXT NOT NULL REFERENCES public.devices(id) ON DELETE CASCADE,
    action_state BOOLEAN NOT NULL,  -- true = ON, false = OFF
    is_active BOOLEAN DEFAULT true NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ----------------------------------------------------
-- 2. ESP32 Hardware Authentication Helper Function
-- ----------------------------------------------------
CREATE OR REPLACE FUNCTION public.check_esp32_auth() 
RETURNS BOOLEAN AS $$
BEGIN
    RETURN (
        current_setting('request.headers', true)::json->>'x-esp32-auth-token' = 
        (SELECT token FROM public.hardware_tokens WHERE name = 'esp32' LIMIT 1)
    );
EXCEPTION WHEN OTHERS THEN
    RETURN FALSE;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

