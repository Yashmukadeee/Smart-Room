-- 5. Establish Strict Security Policies
-- ----------------------------------------------------

-- A. Policies for 'room_users'
CREATE POLICY "Allow users to read their own room mappings" 
ON public.room_users
FOR SELECT 
TO authenticated 
USING (auth.uid() = user_id);

-- B. Policies for 'devices'
CREATE POLICY "Allow read devices for authorized users and ESP32" 
ON public.devices
FOR SELECT
USING (
    public.check_esp32_auth()
    OR EXISTS (
        SELECT 1 FROM public.room_users 
        WHERE room_users.room = devices.room 
        AND room_users.user_id = auth.uid()
    )
);

CREATE POLICY "Allow update devices for authorized users and ESP32" 
ON public.devices
FOR UPDATE
USING (
    public.check_esp32_auth()
    OR EXISTS (
        SELECT 1 FROM public.room_users 
        WHERE room_users.room = devices.room 
        AND room_users.user_id = auth.uid()
    )
)
WITH CHECK (
    public.check_esp32_auth()
    OR EXISTS (
        SELECT 1 FROM public.room_users 
        WHERE room_users.room = devices.room 
        AND room_users.user_id = auth.uid()
    )
);

